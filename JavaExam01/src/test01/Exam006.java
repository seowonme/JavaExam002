package test01;

public class Exam006 {
	
	static int total(int i, int j) {
		return i+j;
	}

	public static void main(String[] args) {
		//메소드명: total, 리턴값: int
		//인수목록: int i, int j
		//처리내용: i와 j를 더한 결과 리턴
		//이 메소드를 호출하여 처리내용 결과를 표시하십시오
		//메소드를 호출 시 인수는 10, 20을 지정합니다.
		
		
		System.out.println(total(10,20));

	}

}
